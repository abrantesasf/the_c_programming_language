#include <stdio.h>

int main(void)
{
    // Expressões viram SENTENÇAS quando terminadas por ";"
    int x = 0;
    x++;
    printf("%d\n", x);

    // Chaves agrupam declarações, definições e sentenças em um único BLOCO,
    // também chamado de SENTENÇA COMPOSTA (um bloco é sintaticamente
    // equivalente a uma sentença única).
    // Obs.: NÃO HÁ ";" após a chave de fechamento de um bloco.
    
}
